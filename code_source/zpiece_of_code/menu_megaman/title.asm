title_print:
	call	BUFCLR
	ld	hl, title_picture
	ld	de, plotsscreen
	ld	bc, title_picture_end - title_picture
	ldir
	call	BUFCOPY
	ld	a, 44
	ld	(penrow),a
	ld	a, 24
	ld	(pencol),a
	ld	hl, press_any_key
	call	_vputs
	call	_getkey
	ret

